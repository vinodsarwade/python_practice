import math
'''ceil'''
#it will upgrade the floting number ex: 2.1 -> 3, 2.8-> 3
#if any floating point above number then it gives next number. 
num1 = 2.3
result = math.ceil(num1)
print(result)

'''floor'''
#it will goes to lower limit ex: 2.99 -> 2, 2.1-> 2
num2 = 2.99
res = math.floor(num2)
print(res)