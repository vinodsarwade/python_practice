# for loop  can iterate over a sequence of iterable objects in python.
# iterating over sequence is nothing but iterating over string.{list, tuple, set, dict}


# for loop for string
name = "vinod"
for i in name:
    print(i)

# for loop for list
colors = ["Red","Green","Blue","Yellow"]
for color in colors:             # it will print all the color in colors variable.
    print(color)
    for i in color:   # it will iterate over each string and print each charactor
        print(i)




    